def get_routes(data, manager, routing, solution):
    routes = []
    for vehicle_id in range(data["num_vehicles"]):
        index = routing.Start(vehicle_id)
        route = []
        while not routing.IsEnd(index):
            route.append(manager.IndexToNode(index))
            index = solution.Value(routing.NextVar(index))
        route.append(manager.IndexToNode(index))
        routes.append(route)
    return routes


def print_solution(data, manager, routing, solution):
    """Prints solution on console."""
    print(f"Objective: {solution.ObjectiveValue()}")
    max_route_distance = 0
    for vehicle_id in range(data["num_vehicles"]):
        index = routing.Start(vehicle_id)
        plan_output = f"Route for vehicle {vehicle_id}:\n"
        route_distance = 0
        while not routing.IsEnd(index):
            plan_output += f" {manager.IndexToNode(index)} -> "
            previous_index = index
            index = solution.Value(routing.NextVar(index))
            route_distance += routing.GetArcCostForVehicle(
                previous_index, index, vehicle_id
            )
        plan_output += f"{manager.IndexToNode(index)}\n"
        plan_output += f"Distance of the route: {route_distance}m\n"
        print(plan_output)
        max_route_distance = max(route_distance, max_route_distance)
    print(f"Maximum of the route distances: {max_route_distance}m")
    

def createGraph(routes):
    graph={}
    for route in routes:
        for i in range(len(route)-1):
            if route[i] not in graph:
                graph[route[i]]=[route[i+1]]
            else:
                graph[route[i]].append(route[i+1])
            if route[i+1] not in graph:
                graph[route[i+1]]=[route[i]]
            else:
                graph[route[i+1]].append(route[i])
    return graph    

def get_busRoute(start_node,end_node,graph):    
    def get_traversal(start,end,path=[]):
        path = path + [start]
        if start == end:
            return path
        if start not in graph:
            print("Not in graph")
            return []
        for node in graph[start]:
            if node not in path:
                newpath = get_traversal(node, end, path)
                if newpath: return newpath
        return []
    
    path = get_traversal(start_node, end_node)
    if path:
        return path
    else:
        print("No path exists between", start_node, "and", end_node)
        return []
        
